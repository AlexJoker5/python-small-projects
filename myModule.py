# Celsius to Fahrenheit
import math


def cToF(c=0):
    return (c*9)/5 + 32
# Fahrenheit to Celsius
def fToC(f=0):
    return ((f-32)*5)/9
# Kilograms to pound
def kgToLb(kg):
    return kg*2.2
# Pound to kilograms
def lbToKg(lb):
    return lb/2.2
# Pound to Ounces
def lbToOunce(lb):
    return lb*16
# Ounces to Pound
def ounceToLb(ounce):
    return ounce/16
# Kilometers to miles
def kmToMile(km):
    return km/1.609
# Feet to inches
def ftToInch(ft=0):
    return ft*12
# Centimeters to meters
def cmToM(cm):
    return cm/100


